// augmented addition
let flower = 1;
console.log(flower);
//flower = flower + 3;
flower += 3;
console.log(flower);

// augmented subtraction
let apple = 3;
console.log(apple);
apple -= 2;
console.log(apple);

//augmented multiplication
let orange = 5;
console.log(orange);
orange *= 6;
console.log(orange);


//division
let chocolate = 8;
console.log(chocolate);
chocolate /= 2;
console.log(chocolate);
